import tileSetUrl from "../assets/tileset.png";

export const loadImage = (src: string) =>
  new Promise<HTMLImageElement>((resolve, reject) => {
    const img = new Image();
    img.onload = () => resolve(img);
    img.onerror = reject;
    img.src = src;
  });

export const getSpriteSheet = () => loadImage(tileSetUrl);

